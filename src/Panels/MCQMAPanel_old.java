    private void OkActionPerformed(java.awt.event.ActionEvent evt) {                                   
        String statement = this.QuestionTextArea.getText();
        ArrayList<String> answers = new ArrayList(); 
        int capacity = ((DefaultListModel)this.AnswerList.getModel()).getSize();
        for(int i=0;i<capacity;i++){
            answers.add((String)((DefaultListModel)this.AnswerList.getModel()).getElementAt(i));
        }
        this.question = new MCQMA(statement, answers, null ); 
        System.out.println(this.question);
        ((MakeMCQMAQuestion)this.frame).setQ(this.question); 
                 
    }                                  

    private void ClearActionPerformed(java.awt.event.ActionEvent evt) {                                      
        this.QuestionTextArea.setText(""); 
        ((DefaultListModel)this.AnswerList.getModel()).removeAllElements();
        this.jLabel3.setText("");
         
    }                                     

