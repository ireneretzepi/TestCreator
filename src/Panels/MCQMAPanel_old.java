    private void EditAnswerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditAnswerActionPerformed
        int index = AnswerList.getSelectedIndex();
      if(index != -1){
          String answer = JOptionPane.showInputDialog("Enter an answer"); 

          ((DefaultListModel)this.AnswerList.getModel()).remove(index);
        ((DefaultListModel)this.AnswerList.getModel()).add(index, answer);          
      }
        
    }//GEN-LAST:event_EditAnswerActionPerformed

